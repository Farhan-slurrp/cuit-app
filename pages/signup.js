import { useState } from "react";
import { useGlobalContext } from "../custom-hooks/context";
import Head from "next/head";

const SignUp = () => {
  const [data, setData] = useState(null);
  const [visibility, setVisibility] = useState(false);
  const { tokenInfo, onSignup } = useGlobalContext();

  const handleSubmit = async (e) => {
    e.preventDefault();
    const payload = await onSignup(e);
    setData(payload);
    console.log(tokenInfo, data);
  };

  return (
    <>
      <Head>
        <title>Cuit - Homepage</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/c.png" />
      </Head>
      <div className="userauth-container">
        <h1>
          {"{"}Make New Account.{"}"}
        </h1>
        <div className="form-container">
          <form
            autoComplete="off"
            id="register-form"
            className="form-body"
            onSubmit={(e) => handleSubmit(e)}
          >
            <label htmlFor="username">Username: </label>
            <input type="text" name="username" required />
            <label htmlFor="email">Email: </label>
            <input type="email" name="email" required />
            <label htmlFor="password">Password: </label>
            <input
              type={visibility ? "text" : "password"}
              name="password"
              required
            />
            <button
              onClick={() => setVisibility(!visibility)}
              className="togglePassword"
            >
              {visibility ? "Hide Password" : "Show Password"}
            </button>
          </form>
        </div>
        <div className="submit-btn">
          <button type="submit" form="register-form">
            Sign Up
          </button>
        </div>
        {data && data.success == false && (
          <p className="error-msg">"{data.msg}"</p>
        )}
      </div>
    </>
  );
};

export default SignUp;
